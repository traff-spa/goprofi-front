// Function to get professions by type ID with fallback to default
export const getProfessionsForType = (typeId: number | undefined): ProfessionCategory[] => {
    if (!typeId) return professionsByType[0];
    return professionsByType[typeId] || professionsByType[0];
};

// Function to get type details by ID with fallback to default
export const getTypeDetails = (primaryTypeId: number | undefined, wingTypeId: number | undefined): TypeDetails => {
    // If we don't have primary type, return default (type 7 since it's fully detailed)
    if (!primaryTypeId) return typeDetailsByType[7];

    // Get the primary type details or default to type 7
    const primaryDetails = typeDetailsByType[primaryTypeId] || typeDetailsByType[7];

    // If no wing type, return primary type details
    if (!wingTypeId || wingTypeId === primaryTypeId) return primaryDetails;

    // If wing type exists, we can customize or merge details, for now just return primary
    // In the future you could merge traits or highlight the influence of the wing
    return primaryDetails;
};